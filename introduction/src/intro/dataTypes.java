package intro;

public class dataTypes {
    public static void main(String[] args) {
        // Primitive Data Types
        int intVar = 42; // Integer
        double doubleVar = 3.14; // Floating-point number
        char charVar = 'A'; // Character
        boolean boolVar = true; // Boolean

        // Non-Primitive Data Types
        String stringVar = "Hello, World!"; // String

        // Displaying the values
        System.out.println("Integer: " + intVar);
        System.out.println("Double: " + doubleVar);
        System.out.println("Character: " + charVar);
        System.out.println("Boolean: " + boolVar);
        System.out.println("String: " + stringVar);
    }
}
